#Argumentos

#Para indicar que una cadena actue como separador se usa sep

print("hola", "mundo", sep="___")

#Argumento Posicionales
#Estos son argumentos sin nombre

separador = "---" #separa las palabras
fin = "FIN" #muestra el caracter de fin de la linea "end"

print("Hola", "Mundo", "bye", sep=separador, end=fin)
